public class Clase6_BucleFor {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println ("El valor de la cifra es: " + i);
        }

        for (int i = 10; i >= 1; i--) {
            System.out.println ("El valor de la cifra es: " + i);
        }

    }
}
